num_inteiro = int(input('Entre com um número inteiro: '))

soma = ((3 * num_inteiro) + 1) + ((2 * num_inteiro) - 1)

print(f'Sucessor do triplo de {num_inteiro}: {(3 * num_inteiro) + 1}')
print(f'Antecessor do dobro de {num_inteiro}: {(2 * num_inteiro) - 1}')

print(f'A soma do sucessor do triplo de {num_inteiro} com o antecessor do dobro de {num_inteiro} é: {soma}')
